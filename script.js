function renderProfile() {
    if (!currentUser) {
        app.innerHTML = `<div class="empty-state">🔒 Пожалуйста, войдите, чтобы увидеть профиль.</div>`;
        return;
    }
    const userReviews = reviews.filter(r => r.username === currentUser.username);
    app.innerHTML = `
        <div class="detail-container" style="padding:30px">
            <h2>👤 Профиль: ${currentUser.username}</h2>
            <p>📧 Email: ${currentUser.email}</p>
            <p>📝 Моих отзывов: ${userReviews.length}</p>
            <p>⭐ Избранных рецептов: ${favorites.length}</p>
            <button id="clearFavsBtn" style="margin-top:20px; background:#e67e22; border:none; padding:10px 20px; border-radius:40px; cursor:pointer;">Очистить избранное</button>
        </div>
    `;
    document.getElementById("clearFavsBtn")?.addEventListener("click", () => { favorites = []; saveFavs(); renderProfile(); });
}

function renderApp() {
    if (currentPage === "catalog") renderCatalog();
    else if (currentPage === "favorites") renderFavorites();
    else if (currentPage === "profile") renderProfile();
    updateNav();
}

function updateNav() {
    document.querySelectorAll(".nav-link").forEach(link => {
        const page = link.getAttribute("data-page");
        if (page === currentPage) link.classList.add("active");
        else link.classList.remove("active");
    });
}

document.querySelectorAll(".nav-link").forEach(link => {
    link.addEventListener("click", (e) => {
        e.preventDefault();
        const page = link.getAttribute("data-page");
        if (page === "catalog") {
            hasSearched = false;
            activeCuisine = "Все";
            activeMeal = "Все";
            searchText = "";
            currentPageNum = 1;
        }
        currentPage = page;
        renderApp();
        window.history.pushState({}, "", `#${page}`);
    });
});

const loginModal = document.getElementById("loginModal");
const registerModal = document.getElementById("registerModal");
document.getElementById("loginBtn").onclick = () => loginModal.style.display = "flex";
document.getElementById("registerBtn").onclick = () => registerModal.style.display = "flex";
document.querySelectorAll(".close").forEach(close => {
    close.onclick = () => { loginModal.style.display = "none"; registerModal.style.display = "none"; };
});
window.onclick = (e) => { if (e.target === loginModal) loginModal.style.display = "none"; if (e.target === registerModal) registerModal.style.display = "none"; };

document.getElementById("doLoginBtn").onclick = () => {
    const username = document.getElementById("loginUsername").value;
    const pwd = document.getElementById("loginPassword").value;
    const user = users.find(u => u.username === username && u.password === pwd);
    if (user) {
        currentUser = user;
        saveCurrentUser();
        updateAuthUI();
        loginModal.style.display = "none";
        renderApp();
    } else document.getElementById("loginError").innerText = "Неверный логин или пароль";
};
document.getElementById("doRegisterBtn").onclick = () => {
    const username = document.getElementById("regUsername").value;
    const email = document.getElementById("regEmail").value;
    const pwd = document.getElementById("regPassword").value;
    if (username.length < 3 || pwd.length < 4 || !email.includes("@")) {
        document.getElementById("regError").innerText = "Логин (≥3), пароль (≥4), email корректный";
        return;
    }
    if (users.find(u => u.username === username)) {
        document.getElementById("regError").innerText = "Пользователь уже существует";
        return;
    }
    const newUser = { username, email, password: pwd };
    users.push(newUser);
    saveUsers();
    currentUser = newUser;
    saveCurrentUser();
    updateAuthUI();
    registerModal.style.display = "none";
    renderApp();
};
document.getElementById("logoutBtn").onclick = () => {
    currentUser = null;
    saveCurrentUser();
    updateAuthUI();
    currentPage = "catalog";
    hasSearched = false;
    renderApp();
};

function handleHash() {
    const hash = window.location.hash.slice(1);
    if (hash === "favorites") currentPage = "favorites";
    else if (hash === "profile") currentPage = "profile";
    else currentPage = "catalog";
    renderApp();
}

